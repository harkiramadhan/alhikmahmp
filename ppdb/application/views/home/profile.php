                Berangkat dari firman Allah tersebut SDIT Al Hikmah lahir pada tahun 1994, atas prakarsa ust Abdul Hasib Hasan, Lc.   Di tahun pertama berdirinya SDIT Al Hikmah menempati gedung milik Yayasan Al Andalusia yang terletak di jalan Bangka II F. <br> <br>
                Setelah gedung milik Yayasan Al Hikmah berdiri,  maka pada tahun  1995 SDIT Al Hikmah menempati  gedung baru yang terletak di jalan Bangka II  No. 24  tepatnya di belakang gedung MI. sampai tahun 1998 sambil menunggu selesainya renovasi total gedung lama yang sebelumnya digunakan untuk Madrasah Aliyah dan Madrasah Tsanawiyah. <br> <br>
                Seiring dengan pelepasan angkatan pertama dan selesainya renovasi gedung, maka pada tahun 1998 SDIT pindah ke blok baru yang  memiliki 4 lantai.  Sementara Mts pindah ke gedung belakang dan Aliyah  di lantai 4 satu blok dengan SDIT.
